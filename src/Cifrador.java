    private void botao_waterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_waterActionPerformed
        String imagemOriginal;
        char[] arquivoLido = new char[TAM];
        char c = 0;
        int totalLido = 0;
        

        imagemOriginal = imagem_original.getText();

        DataInputStream leitura;
        try {
            leitura = new DataInputStream(new FileInputStream(imagemOriginal));
            do {
                for (int i = 0; i < TAM; i++) {
                    c = (char) leitura.readByte();
                    arquivoLido[i] = c;
                    totalLido = i;
                    totalBytesLidos +=1;
                }

                if (totalLido != 0) {
                    water(arquivoLido,totalBytesLidos);
                }

            } while (arquivoLido != null);

            leitura.close();

        } catch (IOException e) {
            water(arquivoLido, totalBytesLidos);
        }

    }//GEN-LAST:event_botao_waterActionPerformed

