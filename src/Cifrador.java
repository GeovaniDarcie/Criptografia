    private void botao_decifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_decifrarActionPerformed
        String arquivoCifrado;
        char[] arquivoLido = new char[TAM];
        char c = 0;
        int totalLido = 0;

        DataInputStream leitura;

        arquivoCifrado = decifrar_campo1.getText();

        try {

            leitura = new DataInputStream(new FileInputStream(arquivoCifrado));

            do {

                for (int i = 0; i < TAM; i++) {
                    c = (char) leitura.readByte();
                    arquivoLido[i] = c;
                    totalLido = i;
                }

                if (totalLido != 0) {
                    decifrador(arquivoLido);
                }
            } while (c != 0);

            leitura.close();

        } catch (EOFException eof) {

        } catch (IOException e) {

        }
    }//GEN-LAST:event_botao_decifrarActionPerformed

