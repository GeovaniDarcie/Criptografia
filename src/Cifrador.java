    public void cifrador(char[] arquivoLido) {
        String codigo, arquivoCifrado;
        char[] cifra = new char[TAM];

        DataOutputStream escrita;

        codigo = campo_codigo.getText();
        arquivoCifrado = campo_cifragem.getText();

        //converte
        char[] senha;
        senha = new char[codigo.length()];
        for (int i = 0; i < codigo.length(); i++) {
            senha[i] = codigo.charAt(i);
        }

        //cifra
        for (int i = 0; i < TAM; i++) {

            cifra[i] = (char) (arquivoLido[i] + senha[i]);

        }

        //escrever no arquivo  
        try {
            escrita = new DataOutputStream(new FileOutputStream(arquivoCifrado, true));
            for (int i = 0; i < TAM; i++) {
                escrita.writeByte(cifra[i]);
            }
            escrita.close();
        } catch (IOException e) {

        }
    }

    public void decifrador(char[] arquivoLido) {
        String codigo, arquivoDecifrado;
        char[] decifrado = new char[TAM];

        DataOutputStream escrita;

        codigo = decifrar_campo3.getText();
        arquivoDecifrado = decifrar_campo2.getText();
        //converte
        char[] senha;
        senha = new char[codigo.length()];
        for (int i = 0; i < codigo.length(); i++) {
            senha[i] = codigo.charAt(i);
        }

        //decifra
        for (int i = 0; i < TAM; i++) {

            decifrado[i] = (char) (arquivoLido[i] - senha[i]);
        }

        //escrever no arquivo  
        try {
            escrita = new DataOutputStream(new FileOutputStream(arquivoDecifrado, true));
            for (int i = 0; i < TAM; i++) {
                escrita.writeByte(decifrado[i]);
            }

            escrita.close();
        } catch (IOException e) {

        }
    }

